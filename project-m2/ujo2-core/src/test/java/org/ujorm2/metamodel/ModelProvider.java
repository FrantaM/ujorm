package org.ujorm2.metamodel;

import javax.annotation.Nonnull;
import org.ujorm2.core.MetaDomainStore;
import org.ujorm2.core.PDomain;
import org.ujorm2.doman.Anonymous;
import org.ujorm2.doman.Item;
import org.ujorm2.doman.Order;
import org.ujorm2.doman.User;

/**
 * TODO: A helper class generated by a Maven module.
 * @author Pavel Ponec
 */
public final class ModelProvider {

    final MetaDomainStore factory = new MetaDomainStore();
    final PDomain<MetaItem> item = factory.newModel();
    final PDomain<MetaOrder> order = factory.newModel();
    final PDomain<MetaUser> user = factory.newModel();
    final PDomain<MetaAnonymous> anonumous = factory.newModel();

    public ModelProvider() {
        factory.close(this);
    }

    /** provide  */
    @Nonnull
    public MetaDomainStore getModels$() {
        return factory;
    }

    public MetaItem<Item> item() {
        return item.model();
    }

    public MetaOrder<Order> order() {
        return order.model();
    }

    public MetaUser<User> user() {
        return user.model();
    }

    public MetaAnonymous<Anonymous> anonumous() {
        return anonumous.model();
    }
}
